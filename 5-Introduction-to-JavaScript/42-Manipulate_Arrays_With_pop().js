//Learned that with .pop() the value at the end of an array can be removed and
//if needed that removed value can be stored in a variable.
// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

